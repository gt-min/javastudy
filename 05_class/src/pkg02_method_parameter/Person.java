package pkg02_method_parameter;

public class Person {

  /*
   * 메소드의 구성
   * 1. 반환타입 : 메소드를 호출하면 메소드가 실행되고 어떤 값을 반환하는데 그 값의 타입
   * 2. 메소드명
   * 3. 매개변수 : parameter 라고 한다. 메소드를 호출할 때 전달되는 값(인자, argument)을 저장하는 변수
   */
  
  /*
   * 메소드의 형식
   *   반환타입 메소드명(매개변수) {
   *     메소드 본문
   *   }
   */

  void method1(int number) {
    System.out.println("method1:" + number);
  }
  
  
  
  
  
  
  
  
  
  
  
}
